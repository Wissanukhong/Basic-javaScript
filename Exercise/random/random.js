// Math random beween 1 - 10
let random1 = Math.random() * 10;
console.log(random1);

// Math random beween 1 - 10 (both included)
let random2 = Math.floor(Math.random() * 10);
console.log(random2);

// Math random beween 1 - 100 (both included)
let random3 = Math.floor(Math.random() * 101);
console.log(random3);

// Math random beween 1 - 100
let random4 = Math.floor(Math.random() * 100) + 1;
console.log(random4);
